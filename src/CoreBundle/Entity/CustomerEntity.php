<?php

namespace CoreBundle\Entity;

use Doctrine\Common\Collections\ArrayCollection;
use Doctrine\ORM\Mapping as ORM;

/**
 * @ORM\Entity
 * @ORM\Table(name="customer")
 */
class CustomerEntity extends SecurityEntity
{

    /**
     * @ORM\Column(name="firstName", type="string", length=50)
     */
    private $firstName;

    /**
     * @ORM\Column(name="lastName", type="string", length=50)
     */
    private $lastName;

    /**
     * @ORM\ManyToOne(targetEntity="CoreBundle\Entity\AddressEntity", cascade={"persist"})
     * @ORM\JoinColumn(name="addressId", referencedColumnName="id")
     */
    private $address;

    /**
     * @ORM\OneToMany(targetEntity="CoreBundle\Entity\OrderEntity", mappedBy="customer", fetch="EAGER")
     */
    private $orders;


    public function __construct()
    {
        $this->orders = new ArrayCollection();
    }

    public function getFirstName()
    {
        return $this->firstName;
    }

    public function setFirstName($firstName): CustomerEntity
    {
        $this->firstName = $firstName;
        return $this;
    }

    public function getLastName()
    {
        return $this->lastName;
    }

    public function setLastName($lastName): CustomerEntity
    {
        $this->lastName = $lastName;
        return $this;
    }

    public function getAddress(): AddressEntity
    {
        return $this->address;
    }

    public function setAddress(AddressEntity $address): CustomerEntity
    {
        $this->address = $address;
        return $this;
    }

    public function getOrders()
    {
        return $this->orders;
    }

    public function __toString()
    {
        return (String)sprintf("Customer(%s)",
            implode(", ", [
                $this->getId(),
                $this->firstName,
                $this->lastName,
                $this->getEmail()])
        );
    }

    public function getRoles()
    {
        return parent::getRoles(); // TODO: Change the autogenerated stub
    }
}